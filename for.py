# 遍历数字
i = 1
for i in range(i,10):
    print(i," ",end="")
print("\n--------------------")

# 遍历集合
list = [1,2,3,4]
for t in list:
    print(t,"",end="")
print("\n--------------------")

# 遍历元组
tuple = (1,2,3,4)
for t in tuple:
    print(t,"",end="")
print("\n--------------------")

# 双for循环
x = 1
for x in range(x,10):
    y = 1
    for y in range(y,10):
        print(x*y," ",end="")
    print()
print("\n--------------------")

# 遍历字符串
str = "abcdef"
for s in str:
    print(s," ",end="")
print("\n--------------------")
